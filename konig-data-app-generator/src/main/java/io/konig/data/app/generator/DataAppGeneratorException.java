package io.konig.data.app.generator;

public class DataAppGeneratorException extends Exception {
	private static final long serialVersionUID = 1L;

	public DataAppGeneratorException(String msg) {
		super(msg);
	}

	public DataAppGeneratorException(Throwable cause) {
		super(cause);
	}

	public DataAppGeneratorException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
