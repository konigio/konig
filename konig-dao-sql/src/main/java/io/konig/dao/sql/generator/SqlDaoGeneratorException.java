package io.konig.dao.sql.generator;

public class SqlDaoGeneratorException extends Exception {

	private static final long serialVersionUID = 1L;


	public SqlDaoGeneratorException(String arg0) {
		super(arg0);
	}

	public SqlDaoGeneratorException(Throwable arg0) {
		super(arg0);
	}

	public SqlDaoGeneratorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
