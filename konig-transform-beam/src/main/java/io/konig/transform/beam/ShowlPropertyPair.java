package io.konig.transform.beam;

import io.konig.core.showl.ShowlPropertyShape;

public class ShowlPropertyPair {
	private ShowlPropertyShape left;
	private ShowlPropertyShape right;
	
	
	public ShowlPropertyShape getLeft() {
		return left;
	}
	public void setLeft(ShowlPropertyShape left) {
		this.left = left;
	}
	public ShowlPropertyShape getRight() {
		return right;
	}
	public void setRight(ShowlPropertyShape right) {
		this.right = right;
	}
	
	

	

}
