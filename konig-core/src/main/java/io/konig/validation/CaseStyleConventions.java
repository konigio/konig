public class CaseStyleConventions {

	private CaseStyle classes;
	private CaseStyle properties;
	private CaseStyle namedIndividuals;
	private CaseStyle nodeShapes;

	public CaseStyle getClasses() {
		return classes;
	}

	public void setClasses(CaseStyle classes) {
		this.classes = classes;
	}

	public CaseStyle getProperties() {
		return properties;
	}

	public void setProperties(CaseStyle properties) {
		this.properties = properties;
	}

	public CaseStyle getNamedIndividuals() {
		return namedIndividuals;
	}

	public void setNamedIndividuals(CaseStyle namedIndividuals) {
		this.namedIndividuals = namedIndividuals;
	}


	public CaseStyle getNodeShapes() {
		return nodeShapes;
	}

	public void setNodeShapes(CaseStyle nodeShapes) {
		this.nodeShapes = nodeShapes;
	}

	
	
}
