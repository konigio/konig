	public static String quote(String word) {
		if (word.contains(".")) {
			String[] array = word.split("\\.");
			StringBuilder builder = new StringBuilder();
			String dot="";
			for (String w : array) {
				builder.append(dot);
				dot=".";
				if (KEYWORDS.contains(w.toUpperCase())) {
					builder.append('`');
					builder.append(w);
					builder.append('`');
				} else {
					builder.append(w);
				}
			}
			return builder.toString();
			
		} else if (KEYWORDS.contains(word.toUpperCase())) {
			StringBuilder builder = new StringBuilder();
			builder.append('`');
			builder.append(word);
			builder.append('`');
			word = builder.toString();
		}
		
		return word;
	}
